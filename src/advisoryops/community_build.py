def _sort_feed_entries(rows: List[Dict[str, Any]]) -> List[Dict[str, Any]]:
    return sorted(
        rows,
        key=lambda r: (
            -int(r.get("score", 0) or 0),
            str(r.get("last_seen_at", "") or ""),
            str(r.get("issue_id", "") or ""),
        ),
    )


def _rss_pub_date(date_str: str) -> str:
    """Convert YYYY-MM-DD (or ISO 8601) to RFC 2822 for RSS pubDate."""
    if not date_str:
        return ""
    # Strip time portion if present
    ds = date_str[:10]
    try:
        dt = datetime(int(ds[0:4]), int(ds[5:7]), int(ds[8:10]), tzinfo=timezone.utc)
        return dt.strftime("%a, %d %b %Y %H:%M:%S +0000")
    except (ValueError, IndexError):
        return ""


def _write_rss(
    path: Path,
    rows: List[Dict[str, Any]],
    *,
    top: int = 50,
    title: str = "AdvisoryOps Community Feed",
    description: str = "Top-scored healthcare cybersecurity and device-safety advisories",
) -> None:
    """Write an RSS 2.0 feed of the top *top* scored issues and validate XML."""
    path.parent.mkdir(parents=True, exist_ok=True)

    channel = ET.Element("channel")

    ET.SubElement(channel, "title").text = title
    ET.SubElement(channel, "link").text = "https://github.com/advisoryops/advisoryops"
    ET.SubElement(channel, "description").text = description
    now_rfc = datetime.now(tz=timezone.utc).strftime("%a, %d %b %Y %H:%M:%S +0000")
    ET.SubElement(channel, "lastBuildDate").text = now_rfc
    ET.SubElement(channel, "language").text = "en-us"

    for issue in rows[:top]:
        item = ET.SubElement(channel, "item")

        ET.SubElement(item, "title").text = issue.get("title") or ""

        link = issue.get("canonical_link") or ""
        ET.SubElement(item, "link").text = link
        ET.SubElement(item, "guid").text = link or issue.get("issue_id", "")

        published_dates = issue.get("published_dates") or []
        first_date = published_dates[0] if published_dates else ""
        pub_date = _rss_pub_date(first_date)
        if pub_date:
            ET.SubElement(item, "pubDate").text = pub_date

        summary = (issue.get("summary") or "")[:500]
        ET.SubElement(item, "description").text = summary

        priority = issue.get("priority") or ""
        if priority:
            ET.SubElement(item, "category").text = priority

    rss_root = ET.Element("rss", version="2.0")
    rss_root.append(channel)

    xml_bytes = b'<?xml version="1.0" encoding="UTF-8"?>\n' + ET.tostring(
        rss_root, encoding="unicode"
    ).encode("utf-8")

    # Validate before writing
    ET.fromstring(xml_bytes)

    path.write_bytes(xml_bytes)


def _write_csv(path: Path, rows: List[Dict[str, Any]]) -> None:
    path.parent.mkdir(parents=True, exist_ok=True)
    fieldnames = [
        "issue_id",
        "issue_type",
        "priority",
        "score",
        "title",
        "canonical_link",
        "cves",
        "sources",
        "published_dates",
        "first_seen_at",
        "last_seen_at",
    ]
    with path.open("w", encoding="utf-8", newline="") as f:
        writer = csv.DictWriter(f, fieldnames=fieldnames)
        writer.writeheader()
        for row in rows:
            writer.writerow(
                {
                    "issue_id": row.get("issue_id", ""),
                    "issue_type": row.get("issue_type", ""),
                    "priority": row.get("priority", ""),
                    "score": row.get("score", 0),
                    "title": row.get("title", ""),
                    "canonical_link": row.get("canonical_link", ""),
                    "cves": ";".join(row.get("cves", []) or []),
                    "sources": ";".join(row.get("sources", []) or []),
                    "published_dates": ";".join(row.get("published_dates", []) or []),
                    "first_seen_at": row.get("first_seen_at", ""),
                    "last_seen_at": row.get("last_seen_at", ""),
                }
            )


def _generate_dashboard(path: Path) -> None:
    """Write the static dashboard HTML file to *path*.

    The dashboard loads ``feed_latest.json``, ``alerts_public.jsonl``, and
    ``meta.json`` via ``fetch()`` from the same directory.  All trust-layer
    sections (handling warnings, evidence gaps, source consensus, evidence
    completeness, extracted/inferred badges, source authority) are rendered
    in the detail panel and gracefully hidden when data is absent.
    """
    html = _DASHBOARD_HTML
    path.parent.mkdir(parents=True, exist_ok=True)
    path.write_text(html, encoding="utf-8", newline="\n")


def _deploy_docs(dashboard_path: Path, feed_path: Path, sources_path: Path) -> None:
    """Copy dashboard and data files to ``docs/`` for GitHub Pages deployment.

    Creates ``docs/index.html`` (renamed from dashboard.html),
    ``docs/feed_latest.json``, and ``docs/validated_sources.json``.
    """
    # Walk up from the dashboard to find the repo root (contains .git)
    repo_root: Optional[Path] = None
    candidate = dashboard_path.parent
    for _ in range(10):
        if (candidate / ".git").exists():
            repo_root = candidate
            break
        if candidate.parent == candidate:
            break
        candidate = candidate.parent

    if repo_root is None:
        # Fallback: use cwd or skip silently (e.g. during tests in tmp_path)
        return

    docs_dir = repo_root / "docs"
    docs_dir.mkdir(parents=True, exist_ok=True)

    shutil.copy2(str(dashboard_path), str(docs_dir / "index.html"))
    if feed_path.exists():
        shutil.copy2(str(feed_path), str(docs_dir / "feed_latest.json"))
    if sources_path.exists():
        shutil.copy2(str(sources_path), str(docs_dir / "validated_sources.json"))


