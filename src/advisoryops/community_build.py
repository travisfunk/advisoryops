"""Stage 6: end-to-end community feed builder.

Orchestrates the full Discover → Correlate → Score → (Recommend) pipeline for
the validated source set defined in ``configs/community_public_sources.json``
and publishes a ready-to-serve feed directory.

Typical invocation::

    advisoryops community-build --set-id gold_pass1 --out-root-community outputs/community_public

What it does
------------
1. (Optional ``--refresh``) Runs ``source_run`` for each source in the selected
   validated set to pull new items before building.
2. Runs ``correlate`` across all discover outputs for the validated source IDs,
   writing intermediate results to ``<out_root_community>/correlate/``.
3. Runs ``score_issues`` on the correlated issues, writing to
   ``<out_root_community>/scored/``.
4. Builds the public feed artifacts (see outputs below).
5. (Optional ``--recommend``) Generates JSON remediation packets for P0/P1 alerts
   via the ``recommend_mitigations`` engine.

Output artifacts (all under ``out_root_community/``)::

    issues_public.jsonl      — all scored issues (full feed, sorted by score desc)
    alerts_public.jsonl      — high-priority subset (≥ min_priority)
    feed_latest.json         — most recent N issues as a JSON array (for the dashboard)
    feed.csv                 — spreadsheet-friendly export of all issues
    validated_sources.json   — source registry snapshot (validated + candidates)
    meta.json                — build metadata (params, counts, timestamps)
    packets/                 — per-issue JSON remediation packets (if --recommend)

The dashboard (``dashboard.html``) is generated by ``_generate_dashboard()``
and written alongside the other feed artifacts.  It loads feed_latest.json
and alerts_public.jsonl from the same directory via fetch().
"""
